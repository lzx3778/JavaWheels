package wheels.no_Graphic;

public class Calculate {
	public static double plus(int i, int j) {
		return i + j;
	}

	public static double minus(int i, int j) {
		return i - j;
	}

	public static double times(int i, int j) {
		return i * j;
	}

	public static double divide(int i, int j) {
		return i / j;
	}

}
