package wheels.no_Graphic;

public class CheckBound {

	public static boolean IsBiggerThanZero(int testNumber) {
        return testNumber > 0;
    }

	public static boolean IsNegetive(int testNumber) {
        return testNumber <= 0;
    }

	public static boolean IsPossitive(int testNumber) {
        return testNumber >= 0;
    }
}
